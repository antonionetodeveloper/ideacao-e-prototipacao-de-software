# Python é case sensitive, ou seja, diferencia letras maiusculas de minusculas
# mais sobre em: https://www.w3schools.com/python/python_variables.asp

Teste = "Teste com T maiusculo"
teste = "Teste com t minusculo"

print(Teste)
print(teste)

# percebe-se que o python diferencia as variaveis, pois elas tem nomes diferentes
# ao invés de atribuir o valor, ele cria uma nova variavel
# portanto, são variaveis diferentes
